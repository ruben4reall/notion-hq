export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const val = translations[lang]?.[key] ?? translations['fr']?.[key] ?? key
  if (!vars) return val
  return Object.entries(vars).reduce((s, [k, v]) => s.replace(`{${k}}`, String(v)), val)
}

